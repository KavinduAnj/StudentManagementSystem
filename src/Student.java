public class Student {
    private int id;
    private String name;
    private int age;
    private String courses;
    private double marks;

    public Student(int id,String name,int age,String courses,double marks){
        this.id = id;
        this.name = name;
        this.age = age;
        this.courses = courses;
        this.marks = marks;
    }
}
